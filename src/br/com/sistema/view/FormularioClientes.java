    private void btnPesquisarNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarNomeActionPerformed
         
     
    // Captura o texto da caixa de texto de pesquisa
    String nome = txtPesquisarNome.getText().trim();

    // Instancia o DAO e realiza a pesquisa
    ClientesDAO dao = new ClientesDAO();
    List<Clientes> lista = dao.filtrar(nome); // Chama o método filtrar com o nome para pesquisa

    // Obtém o modelo da tabela e limpa os dados existentes
    DefaultTableModel dados = (DefaultTableModel) tb_clientes.getModel();
    dados.setRowCount(0);

    // Popula a tabela com os resultados da pesquisa
    for (Clientes c : lista) {
        dados.addRow(new Object[]{
            c.getId(),
            c.getNome(),
            c.getRg(),
            c.getCpf(),
            c.getEmail(),
            c.getTelefone(),
            c.getCelular(),
            c.getCep(),
            c.getEndereco(),
            c.getNumero(),
            c.getComplemento(),
            c.getBairro(),
            c.getCidade(),
            c.getEstado()
        });
    }


    
    }//GEN-LAST:event_btnPesquisarNomeActionPerformed
 
    
    
