    public void listar() {
    ClientesDAO dao = new ClientesDAO();
    List<Clientes> lista = dao.listar();
    
    DefaultTableModel dados = (DefaultTableModel) tb_clientes.getModel();
    dados.setRowCount(0); // Limpa os dados da tabela antes de popular
    
    for(Clientes c : lista) {
        dados.addRow(new Object[] {
            c.getId(),
            c.getNome(),
            c.getRg(),
            c.getCpf(),
            c.getEmail(),
            c.getTelefone(),
            c.getCelular(),
            c.getCep(),
            c.getEndereco(),
            c.getNumero(),
            c.getComplemento(),
            c.getBairro(),
            c.getCidade(),
            c.getEstado()
        });
    }
}
    
    
    
    
    
